// This is a short description about the zlang

// 1. types:
// There a six basic types: int, string, boolean, array, map, function
let int = 1;
let string = "a string";
let boolean = true;
let array = [1, 2, 3, 4, 5];
let map = {"k": "v"};
let add = function (a, b) {
    return a + b
};

// 2. array:
// array can hold any types like python
let myarr = [1, 2, true, "test", function (a) {println(a);}]

// use index to get the element
myarr[4]("test println")


// 3. map
// To avoid hash collision map key must be int or string,
// values can store any type you like
let dict = {"chasing": 1020, 1020:"chasing", "chasing1020":[1020,"chasing1020"]}

// you can use this to access it
println(dict["chasing1020"][0])
